def gen_randA(A, N):
    _A = []
    for a in A:
        _a = [a]*N
        for i, _ in enumerate(_a):
            j = np.random.randint(0, N)
            v = min(100-_a[i],100-_a[j],20)
            if v <= 0:
                continue
            offset = np.random.randint(-v, v)
            _a[i] += offset
            _a[j] -= offset
        _A.append(_a)
        assert(np.mean([a]*N)==np.mean(_a))
    return _A

